                <h2>Qué es el software</h2>
            </div>
        </div>
        <div class="row">
            <div class="col-12">
                <p>
                Las personas que nos dedicamos a la programación no deberíamos tener dudas a la hora de definir un concepto tan importante como el "software". La mayoría de nosotros, si tratamos de definirlo, lo haremos de mala manera. Una buena definición de lo que es el software sería “La información, de todo tipo, que das al ordenador para manipular la entrada de datos de un usuario”. <strong>Es decir, el software es información.</strong>
                </p>
                <p>
